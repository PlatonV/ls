#include "ft_ls.h"

unsigned int	ft_numsize(unsigned long n)
{
	unsigned int	result;

	result = 0;
	if (n == 0)
		return (1);
	while (n)
	{
		n /= 10;
		result++;
	}
	return (result);
}
